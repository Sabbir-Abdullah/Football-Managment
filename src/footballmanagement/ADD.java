    private void DONEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DONEActionPerformed
        // TODO add your handling code here:
        try {
        // TODO add your handling code here:
        String query="INSERT INTO `player_info` (`Jersey_No.`, `Name`, `AGE`, `Nationality`, `Formation`, `Club`) VALUES (?, ?, ?, ?, ?,?);";
        // TODO add your handling code here:
        
        conn = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/football management","root", "");
        
        ps=conn.prepareStatement(query);
        ps.setString(1, txtjn.getText());
        ps.setString(2, name.getText());
        ps.setString(3, age.getText());
        ps.setString(4, nat.getText());
        ps.setString(5, fr.getText());
        ps.setString(6, club.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null,"New player added.");
        
        this.hide();
        NEWplayer obj=new NEWplayer();
        obj.setVisible(true);
    } catch (SQLException ex) {
        Logger.getLogger(Signin.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_DONEActionPerformed

