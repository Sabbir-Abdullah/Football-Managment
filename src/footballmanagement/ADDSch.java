    private void DONE1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DONE1ActionPerformed
        // TODO add your handling code here:
        Connection conn=null;
   
        PreparedStatement ps=null;
         try {
        // TODO add your handling code here:
        String query="INSERT INTO `schedule` (`Teams`, `Date`, `Time`) VALUES (?, ?, ?);";
        // TODO add your handling code here:
        
        conn = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/football management","root", "");
        
        ps=conn.prepareStatement(query);
        ps.setString(1, txtteam.getText());
        ps.setString(2, txtdate.getText());
        ps.setString(3, txttime.getText());
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null,"New schedule added.");
        
        this.hide();
        NewSch obj=new NewSch();
        obj.setVisible(true);
    } catch (SQLException ex) {
        Logger.getLogger(ADDSch.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_DONE1ActionPerformed

